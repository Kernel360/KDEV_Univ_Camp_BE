package me.silvernine.tutorial.dto;

//Dto 클래스는 외부와의 통신에 사용할 클래스임
import lombok.*;

import jakarta.validation.constraints.NotNull;
import jakarta.validation.constraints.Size;

@Getter
@Setter
@Builder
@AllArgsConstructor
@NoArgsConstructor
public class LoginDto {

   @NotNull
   @Size(min = 3, max = 50)
   private String Id;

   @NotNull
   @Size(min = 3, max = 100)
   private String password;
}
